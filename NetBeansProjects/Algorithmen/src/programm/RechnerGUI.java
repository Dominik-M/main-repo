    @Override
    public void actionPerformed(ActionEvent e) {
      String text=((JButton)e.getSource()).getText();
      int caret=input.getCaretPosition();
      switch(text){
          case "Enter":
              output.setText("= "+Rechner.rechne(input.getText()));
              return;
          case "Delete":
              input.setText("");
              output.setText("");
              return;
          case "sqrt":
              text="2r()";
              break;
          case "x²":
              text="^2";
              break;
          case "nCr":
              text="n";
              break;
          case "sin":
              text="s()";
              break;
          case "cos":
              text="c()";
              break;
          case "tan":
              text="t()";
              break;
      }
      input.setText(input.getText().substring(0,caret)+
              text+input.getText().substring(caret));
      if(text.contains("()")) {
            caret--;
        }
      input.setCaretPosition(caret+text.length());
    }
