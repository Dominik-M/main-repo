    public void update(){
        if(spieler==null)return;
        name.setText(spieler.toString());
        coins.setText(spieler.getScore()+" Coins");
        String s="Hand:\n";
        for(Karte k:spieler.getKarten())s=s+k.toString()+" ";
        s=s+"\n"+"Ablage:\n";
        for(Karte k:spieler.getAblageKarten())s=s+k.toString()+" ";
        karten.setText(s);
        s="Felder:\n";
        for(int i=0; i<spieler.getFelderAnz(); i++){
            s=s+"("+i+")";
            for(Karte k: spieler.getFeldKarten(i)){
                s=s+" "+k.toString()+" x "+spieler.getFeldKarten(i).length;
                break;
            }
            s=s+"\n";
        }
        felder.setText(s);
    }

